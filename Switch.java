public class Switch {
    public static void main(String[] args) {
        int val = 3;
        switch (val) {
            case 1:
                System.out.println("the val is 2");
                break;
            case 2:
                System.out.println("the val is 2");
                break;
            default:
                System.out.println("default value => no value matched ");
        }
    }
}
